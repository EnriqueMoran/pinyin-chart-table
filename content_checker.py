def check():

	for pinyin in pinyin_list:
		tone1 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}1.mp3"
		tone2 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}2.mp3"
		tone3 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}3.mp3"
		tone4 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}4.mp3"

		req1 = requests.get(tone1)
		req2 = requests.get(tone2)
		req3 = requests.get(tone3)
		req4 = requests.get(tone4)

		if req1.status_code != 200:
			print(f"{tone1} NOT FOUND")

		if req2.status_code != 200:
			print(f"{tone2} NOT FOUND")

		if req3.status_code != 200:
					print(f"{tone3} NOT FOUND")

		if req4.status_code != 200:
					print(f"{tone4} NOT FOUND")


	print("DONE")


def create_json(file_path):
	data = []

	for pinyin in pinyin_list:
		tone1 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}1.mp3"
		tone2 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}2.mp3"
		tone3 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}3.mp3"
		tone4 = f"https://cdn.yoyochinese.com/audio/pychart/{pinyin}4.mp3"

		item =  {
			"pinyin": pinyin,
			"tone1": [tone1],
			"tone2": [tone2],
			"tone3": [tone3],
			"tone4": [tone4]
		}

		data.append(item)

	with open(file_path, 'w') as json_file:
		json.dump(data, json_file, indent=4)

	print("Json file created")


